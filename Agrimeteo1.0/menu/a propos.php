        <h2>Voici comment elle fonctionne: </h2>
        <p> L'application fournit des prévisions météorologiques spécifiques aux zones agricoles. Les agriculteurs peuvent consulter les données de température,
             l'état du ciel, la vitesse du vent et les précipitations pour leur région.
            Ces informations aident les agriculteurs à anticiper les conditions météorologiques et à planifier leurs travaux en conséquence.
            L'application envoie des alertes en temps réel en cas de changements météorologiques soudains. Par exemple, 
            elle peut avertir les agriculteurs d'une tempête imminente, d'une forte pluie ou d'une vague de chaleur.
            Ces notifications aident les agriculteurs à prendre des mesures préventives pour protéger leurs cultures.
